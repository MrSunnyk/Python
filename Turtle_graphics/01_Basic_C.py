import turtle

t=turtle.Turtle()
# turtle.Turtle()  It is used to Call Turtle class provided by the turtle library.
# The = operator assigns  Turtle object to the variable named t.

t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)

turtle.done()
